package com.iss.pojo;

public class ClassInfo {
	private String class_name;
	private String department_id;
	private String class_population;
	private String class_grade;
	private String teacher_id;
	private String tb_student;

	public String getClass_name() {
		return class_name;
	}

	public void setClass_name(String class_name) {
		this.class_name = class_name;
	}

	public String getDepartment_id() {
		return department_id;
	}

	public void setDepartment_id(String department_id) {
		this.department_id = department_id;
	}

	public String getClass_population() {
		return class_population;
	}

	public void setClass_population(String class_population) {
		this.class_population = class_population;
	}

	public String getClass_grade() {
		return class_grade;
	}

	public void setClass_grade(String class_grade) {
		this.class_grade = class_grade;
	}

	public String getTeacher_id() {
		return teacher_id;
	}

	public void setTeacher_id(String teacher_id) {
		this.teacher_id = teacher_id;
	}

	public String getTb_student() {
		return tb_student;
	}

	public void setTb_student(String tb_student) {
		this.tb_student = tb_student;
	}
}
