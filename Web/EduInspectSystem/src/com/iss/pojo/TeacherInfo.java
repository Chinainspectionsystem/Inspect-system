package com.iss.pojo;

public class TeacherInfo {
	private String teacher_id;
	private String department_id;
	private String teacher_name;
	private String gender;
	private String jobtitle_id;
	private String teacher_inspector;

	public String getTeacher_id() {
		return teacher_id;
	}

	public void setTeacher_id(String teacher_id) {
		this.teacher_id = teacher_id;
	}

	public String getDepartment_id() {
		return department_id;
	}

	public void setDepartment_id(String department_id) {
		this.department_id = department_id;
	}

	public String getTeacher_name() {
		return teacher_name;
	}

	public void setTeacher_name(String teacher_name) {
		this.teacher_name = teacher_name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getJobtitle_id() {
		return jobtitle_id;
	}

	public void setJobtitle_id(String jobtitle_id) {
		this.jobtitle_id = jobtitle_id;
	}

	public String getTeacher_inspector() {
		return teacher_inspector;
	}

	public void setTeacher_inspector(String teacher_inspector) {
		this.teacher_inspector = teacher_inspector;
	}
}
